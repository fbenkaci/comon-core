#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	int		len_s;
	int		i;

	i = 0;
	len_s = ft_strlen(s);
	dest = malloc(len_s * sizeof(char) + 1);
	if (dest == NULL)
		return (NULL);
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
